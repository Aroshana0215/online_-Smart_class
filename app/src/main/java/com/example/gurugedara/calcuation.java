package com.example.gurugedara;

public class calcuation {

    protected float dividebyten(float value,float value2)
    {
        float ans = value / value2;
        return ans;

    }
    protected float dividebyfive(float value,float value2){

        float ans = value * value2;
        return ans;

    }
    protected float dividebyfour(float value,float value2){

        float ans = value + value2;
        return ans;

    }
    protected float dividebythree(float value,float value2){

        float ans = value - value2;
        return ans;

    }




}
